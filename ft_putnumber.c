#include "allft.h"

int **ft_putnumber()
{
    int i;
    int j;

    i = 0;
    while (i < g_n)
    {
        j = 0;
        while (j < g_n)
        {
            box[i][j] = box[i][j] + (j + 1);
            j++;
        }
        i++;
    }
    return (box);
}