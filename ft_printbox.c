#include <unistd.h>

void	ft_printbox(int **b, int n)
{
	int	cx;
	int	cy;

	cy = 0;
	while (cy < n)
	{
		cx = 0;
		while (cx < n)
		{
			b[cy][cx] = b[cy][cx] + 48;
			write(1, &b[cy][cx], 1);
			if (cx != n - 1)
				write(1, " ", 1);
			cx++;
		}
		write(1, "\n", 1);
		cy++;
	}
}
